function openAddWindow(){
    let openaddpop = document.getElementById("openaddpop");
    openaddpop.classList.add("openAddpop");
}
function closeAddWindow(){
    let openaddpop = document.getElementById("openaddpop");
    openaddpop.classList.remove("openAddpop");
}
